import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

df=pd.read_csv('haberman.csv')

df.head()

df.columns=['Age','Year','Nodes','Survival']
df

sns.boxplot(x='Survival',y='Age',data=df)

#people with age>77 will actually live lesser than 5years
#people with age <35 will actually live more than 5 years

sns.FacetGrid(df,hue='Survival',height=6).map(sns.histplot,'Age',bins=5,kde=True).add_legend()
plt.xlabel('Age')
plt.ylabel('Percentage of Survival')
plt.title('PFD for age count',size=12)
plt.show()

#maximum percentage of people living less than 5 years are between 45 to 55.
#people between 30-33 years old are living more than 5 years
#people with age >77 will actually live lesser than 5 years

df=df.replace([1,2],[0,1])

df.groupby('Age')['Survival'].sum().sort_values(ascending=False)

sns.jointplot(x='Age',y='Nodes',data=df,hue='Survival',kind='kde')

#age(43 to 53) and nodes(10 to 52) are likely to live less than 5 years

sns.boxplot(x='Survival',y='Nodes',data=df)

sns.FacetGrid(df,hue='Survival',height=6).map(sns.histplot,'Nodes',bins=10,kde=True).add_legend()
plt.xlabel('Nodes')
plt.ylabel('Percentage of Survival')
plt.title('PFD for Nodes count',size=12)
plt.show()

df.groupby('Nodes')['Survival'].sum().sort_values(ascending=False)

df.Nodes.value_counts().sort_values(ascending=False)



